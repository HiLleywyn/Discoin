def by_period(period: str) -> list[dict]:
    """Return all quest templates for ``period`` ('daily' or 'weekly')."""
    return [q for q in QUESTS if q["period"] == period]


def get(quest_id: str) -> dict | None:
    """Return the template for ``quest_id`` or ``None`` if missing."""
    for q in QUESTS:
        if q["quest_id"] == quest_id:
            return q
    return None


def by_trigger(trigger: str) -> list[dict]:
    """Return all quest templates whose trigger matches ``trigger``."""
    return [q for q in QUESTS if q["trigger"] == trigger]
