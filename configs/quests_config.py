"""Quest catalog - templates for daily and weekly rotating quests.

Each template is one concrete quest a user can be assigned for a period.
services/quests.py draws from the appropriate period pool on assignment
and tracks progress as bus events fire through the same trigger labels
as achievements_config.py.

Schema:
    quest_id      - stable id. Never rename.
    name          - short display name.
    description   - one-line blurb shown in the quest card.
    period        - 'daily' or 'weekly'.
    trigger       - logical trigger label (see services/achievements.py).
    target        - integer threshold to complete.
    reward_usd    - USD paid to wallet on claim.
    icon          - single emoji.

The filter field (optional) lets a template key on event payload. The
quests service forwards the event kwargs so handlers can inspect
fields like won=True for gamble/exploit wins.
"""
