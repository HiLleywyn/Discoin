def get(badge_id: str) -> dict | None:
    """Return the catalog entry for ``badge_id`` or ``None`` if missing."""
    for a in ACHIEVEMENTS:
        if a["badge_id"] == badge_id:
            return a
    return None


def by_trigger(trigger: str) -> list[dict]:
    """Return all achievements whose requirement.trigger matches ``trigger``."""
    return [a for a in ACHIEVEMENTS if a["requirement"].get("trigger") == trigger]


def by_category(category: str) -> list[dict]:
    """Return all achievements in ``category`` sorted by ``sort_order``."""
    out = [a for a in ACHIEVEMENTS if a["category"] == category]
    out.sort(key=lambda a: a.get("sort_order", 0))
    return out


def all_ids() -> set[str]:
    """Return the set of every known badge_id. Used for integrity checks."""
    return {a["badge_id"] for a in ACHIEVEMENTS}
