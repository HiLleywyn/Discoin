"""Achievement catalog - canonical source of truth for all Discoin achievements.

Each entry defines a badge that the bot can award when a user hits a milestone.
services/achievements.py syncs this catalog into the `badges` table on cog
load so the API (api/v2/routers/users.py) and the in-bot `,achievements`
command render the same list.

Schema of each entry:
    badge_id      - stable id used across DB, API, and code. Never rename.
    name          - short display name.
    description   - one-line description shown in embeds.
    icon          - single emoji.
    category      - grouping used by the browse command.
    requirement   - human-readable trigger description (stored as JSONB).
    reward_usd    - USD paid to wallet on grant.
    sort_order    - display order within its category (ascending).
    secret        - if True, hidden in the catalog until earned.

The `trigger` field in requirement is a LOGICAL label; the bus-event to
trigger-label map lives in services/achievements.py so a single label can
fan in from multiple bus events (e.g. Discord trade + API trade).
"""
